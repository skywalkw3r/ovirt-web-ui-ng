for (const locale of LOCALES) {
  const hand = loadHand(locale)
  const rules = RULES[locale] ?? []
  const out = { ...hand }
  let added = 0
  for (const [k, v] of Object.entries(en)) {
    if (!out[k]) {
      out[k] = applyRules(v, rules)
      added++
    }
  }
  writeFileSync(resolve(__dirname, `${locale}.json`), JSON.stringify(out, null, 2) + '\n')
  console.log(`${locale}: ${Object.keys(out).length} keys (${added} rule-filled)`)
}
