/**
 * Best-effort rule-based backfill for all non-en locales.
 * Merges hand-authored chunks (locale-data/<locale>/*.json) with phrase rules.
 * Run after translate-backfill.mjs hits API limits, or as a standalone bootstrap.
 */
import { readFileSync, writeFileSync, existsSync, readdirSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { en } from '../../src/i18n/messages/en.ts'

const __dirname = dirname(fileURLToPath(import.meta.url))

const LOCALES = ['es', 'fr', 'de', 'it', 'pt-BR', 'ru', 'zh-CN', 'ja', 'ko', 'tr']

function loadHand(locale) {
  const merged = {}
  const dir = resolve(__dirname, locale)
  if (existsSync(dir)) {
    for (const f of readdirSync(dir).filter((x) => x.endsWith('.json')).sort()) {
      Object.assign(merged, JSON.parse(readFileSync(resolve(dir, f), 'utf8')))
    }
  }
  const single = resolve(__dirname, `${locale}.json`)
  if (existsSync(single)) Object.assign(merged, JSON.parse(readFileSync(single, 'utf8')))
  return merged
}

function applyRules(text, rules) {
  let out = text
  for (const [re, rep] of rules) out = out.replace(re, rep)
  return out
}

